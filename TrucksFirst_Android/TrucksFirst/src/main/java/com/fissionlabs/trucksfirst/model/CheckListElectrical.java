public class CheckListElectrical {

    private String headlight;
    private String indicatorAndHazard;
    private String horn;
    private String wiper;
    private String temperatureOnTemperatureGauge;
    private String alternatorChargerLight;
    private String oilPressureWarningLight;

    public String getHeadlight() {
        return headlight;
    }

    public void setHeadlight(String headlight) {
        this.headlight = headlight;
    }

    public String getIndicatorAndHazard() {
        return indicatorAndHazard;
    }

    public void setIndicatorAndHazard(String indicatorAndHazard) {
        this.indicatorAndHazard = indicatorAndHazard;
    }

    public String getHorn() {
        return horn;
    }

    public void setHorn(String horn) {
        this.horn = horn;
    }

    public String getWiper() {
        return wiper;
    }

    public void setWiper(String wiper) {
        this.wiper = wiper;
    }

    public String getTemperatureOnTemperatureGauge() {
        return temperatureOnTemperatureGauge;
    }

    public void setTemperatureOnTemperatureGauge(String temperatureOnTemperatureGauge) {
        this.temperatureOnTemperatureGauge = temperatureOnTemperatureGauge;
    }

    public String getAlternatorChargerLight() {
        return alternatorChargerLight;
    }

    public void setAlternatorChargerLight(String alternatorChargerLight) {
        this.alternatorChargerLight = alternatorChargerLight;
    }

    public String getOilPressureWarningLight() {
        return oilPressureWarningLight;
    }

    public void setOilPressureWarningLight(String oilPressureWarningLight) {
        this.oilPressureWarningLight = oilPressureWarningLight;
    }
}
