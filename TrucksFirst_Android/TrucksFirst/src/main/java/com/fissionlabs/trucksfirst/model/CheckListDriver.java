public class CheckListDriver {
    private String engineStarting;
    private String engineSound;
    private String exhaustEmission;
    private String clutchWorking;
    private String gearMovement;
    private String brakeEffectiveness;

    public String getEngineStarting() {
        return engineStarting;
    }

    public void setEngineStarting(String engineStarting) {
        this.engineStarting = engineStarting;
    }

    public String getEngineSound() {
        return engineSound;
    }

    public void setEngineSound(String engineSound) {
        this.engineSound = engineSound;
    }

    public String getExhaustEmission() {
        return exhaustEmission;
    }

    public void setExhaustEmission(String exhaustEmission) {
        this.exhaustEmission = exhaustEmission;
    }

    public String getClutchWorking() {
        return clutchWorking;
    }

    public void setClutchWorking(String clutchWorking) {
        this.clutchWorking = clutchWorking;
    }

    public String getGearMovement() {
        return gearMovement;
    }

    public void setGearMovement(String gearMovement) {
        this.gearMovement = gearMovement;
    }

    public String getBrakeEffectiveness() {
        return brakeEffectiveness;
    }

    public void setBrakeEffectiveness(String brakeEffectiveness) {
        this.brakeEffectiveness = brakeEffectiveness;
    }
}
